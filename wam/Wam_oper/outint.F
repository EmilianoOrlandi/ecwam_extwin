!       -----------------------

        IF (GFLAG(55)) THEN
          iglobal=iglobal+1
          IF ((LMESSPASS.AND.LFDB.AND.(IRANK.EQ.IPFGTBL(55)))
     &     .OR.(LMESSPASS.AND.(.NOT.LFDB).AND.(IRANK.EQ.1))
     &     .OR.(.NOT.LMESSPASS)) THEN
