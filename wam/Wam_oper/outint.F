            ITABLE=140
            CALL WGRIBOUT(IU06, ITEST, NGX, NGY, WDIRT3G,
     &                    ITABLE, IPARAM, 0, 0 , 0,
     &                    CDATE, IFCST, MARSTYPE,
     &                    LFDB, CFDBSF, KFDB, LFDBOPEN, IUOUT)
          ENDIF
        ENDIF

