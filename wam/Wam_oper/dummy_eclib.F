! -----------------------------------------------------------------
!
       SUBROUTINE SYMINV 
       WRITE(6,*) 'FATAL ERROR : ATTEMPT TO USE THE DUMMY DEFINITION OF'
       WRITE(6,*) '              SUBROUTINE SYMINV !!!'
       WRITE(6,*) 'THE ROUTINE WAS REPLACED BY A DUMMY'
       WRITE(6,*) 'DEFINITION AS IT SHOULD NOT BE USED IF NO DATA'
       WRITE(6,*) 'ASSIMILATION IS DONE !!!'
       WRITE(6,*) 'THIS ROUTINE IS PART OF THE ECMWF LIBRARY :'
       WRITE(6,*) 'EMOSLIB OR ECLIB '
       CALL ABORT1
       RETURN
       END

