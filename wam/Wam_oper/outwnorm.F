        IF(NFLAG(30).AND.(GFLAG(30).OR.FFLAG(30).OR.PFLAG(30))) THEN
          CALL WAMNORM(MWP2WG,NGX,NGY,ZMISS,0.,FAVG,FMIN,FMAX,
     &     NAVG,LMESSPASS,IRANK,IPFGTBL(30),1,KTAG)
          KTAG=KTAG+1
