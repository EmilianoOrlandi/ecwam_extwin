        IF(NFLAG(27).AND.(GFLAG(27).OR.FFLAG(27).OR.PFLAG(27))) THEN
          CALL WAMNORM(WSPRDG,NGX,NGY,ZMISS,0.,FAVG,FMIN,FMAX,
