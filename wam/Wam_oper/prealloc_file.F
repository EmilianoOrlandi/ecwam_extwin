      SUBROUTINE PREALLOC_FILE(IU06,IBLK,FILENAME)
c
      CHARACTER*3 CIBLK,CEBLK
      CHARACTER*24 COMLINE
      CHARACTER*80 FILENAME 
      INTEGER EBLK

      IF (IBLK.GE.1) THEN
         WRITE(CIBLK,'(I3.3)') IBLK
      ELSE
         CIBLK = '001'
      ENDIF
      NEBLK=14
      EBLK=IBLK/NEBLK
      IF (EBLK.GE.1) THEN
         WRITE(CEBLK,'(I3.3)') EBLK
      ELSE
         WRITE(CEBLK,'(I3.3)') (IBLK*1000)/NEBLK
      ENDIF
      COMLINE='prealloc -f '//CIBLK//'MB'
      IF (EBLK.GE.1) THEN
      COMLINE='prealloc -f '//CIBLK//'MB:'//CEBLK//'MB '
      ELSE
      COMLINE='prealloc -f '//CIBLK//'MB:'//CEBLK//'KB '
      ENDIF

      LNAME = LEN_TRIM(FILENAME)
      IRET=ISHELL(COMLINE//FILENAME(1:LNAME))
C     CALL SYSTEM(COMLINE//FILENAME(1:LNAME))

      WRITE(IU06,*) 'THE FOLLOWING PREALLOCATION HAS BEEN DONE'
      WRITE(IU06,*) 'VIA A CALL TO ISHELL :'
C     WRITE(IU06,*) 'VIA A SYSTEM CALL :'
      WRITE(IU06,*) COMLINE//FILENAME(1:LNAME) 

      RETURN
      END

