      MODULE YOWWIND

      IMPLICIT NONE

!*    **  *WIND* - VARIABLES USED FOR WIND INPUT AND COMPUTATIONS.

      REAL, PARAMETER :: WSPMIN=1.

      CHARACTER*14 CDA 
      CHARACTER*14 CDAWIFL 
      CHARACTER*14 CDATEWO 
      CHARACTER*14 CDATEFL 
      CHARACTER*14, ALLOCATABLE :: CDTNEXT(:) 
      CHARACTER*80 :: CWDFILE  


      INTEGER IIG 
      INTEGER NC
      INTEGER NR
      INTEGER NSTORE
      INTEGER :: IUNITW=0

      REAL, ALLOCATABLE :: UWND(:,:)
      REAL, ALLOCATABLE :: VWND(:,:)
      REAL, ALLOCATABLE :: AIRD(:,:)
      REAL, ALLOCATABLE :: PZIDL(:,:)

      REAL, ALLOCATABLE :: USNEXT(:,:,:)
      REAL, ALLOCATABLE :: DSNEXT(:,:,:)
      REAL, ALLOCATABLE :: ADSNEXT(:,:,:)
      REAL, ALLOCATABLE :: ZIDLNEXT(:,:,:)

      INTEGER, ALLOCATABLE :: ICEMASKNEXT(:,:,:)

!*     VARIABLE.   TYPE.     PURPOSE.
!      ---------   -------   --------
!      *WSPMIN*    REAL      MINIMUM WIND SPEED ALLOWED IN WAM.
!      *CDA*       CHAR*14   DATE OF LAST WINDFIELD READ IN.
!      *CDATEWO*   CHAR*14   DATE OF NEXT WIND FIELD TO BE READ.
!      *CDAWIFL*   CHAR*14   DATE OF NEXT WIND FILE NAME.
!      *CDATEFL*   CHAR*14   DATE OF NEXT WIND FILE TO BE ACCESSED.
!      *CDTNEXT*   CHAR*14   DATE CORRESPONDING TO TEMPORARY STORAGE
!                            (SEE BELOW).
!      *CWDFILE*   CHAR*80   FILENAME FOR FILE CONTAINING WIND SPEED
!                            AND DIRECTION TIMESERIES WHEN USED INSTEAD
!                            OF FORCING FROM INPUT WIND FIELDS
!                            (see USERIN).
!      *IIG*       INTEGER   BLOCK NUMBER OF LAST WIND FIELD READ IN
!      *NC*        INTGER    NUMBER OF COLUMNS IN INPUT WIND ARRAY
!      *NR*        INTEGER   NUMBER OF ROWS    IN INPUT WIND ARRAY
!      *NSTORE*    INTEGER   DIMENSION OF CDTNEXT
!      *IUNITW*    INTEGER   UNIT USED TO OPEN THE INPUT WIND DATA
!      *UWND*      REAL      INPUT U WIND GRID COMPONENT
!      *VWND*      REAL      INPUT V WIND GRID COMPONENT
!      *AIRD*      REAL      INPUT AIR DENSITY GRID
!      *USNEXT*    REAL      TEMPORARY STORAGE OF WIND SPEED
!      *DSNEXT*    REAL      TEMPORARY STORAGE OF WIND DIRECTION
!      *ADSNEXT*   REAL      TEMPORARY STORAGE OF AIR DENSITY 
!      *ZIDLNEXT*  REAL      TEMPORARY STORAGE OF Zi/L
!      *ICEMASKNEXT INTEGER   TEMPORARY STORAGE OF SEA ICE MASK

! ----------------------------------------------------------------------

      END MODULE YOWWIND
