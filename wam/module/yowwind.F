      MODULE YOWWIND

      IMPLICIT NONE

!*    **  *WIND* - VARIABLES USED FOR WIND INPUT AND COMPUTATIONS.

      REAL, PARAMETER :: WSPMIN=1.

      CHARACTER*12 CDA 
      CHARACTER*12 CDAWIFL 
      CHARACTER*12 CDATEWO 
      CHARACTER*12 CDATEFL 
      CHARACTER*12 CDTNEXT 

      INTEGER IIG 
      INTEGER NC
      INTEGER NR

      REAL, ALLOCATABLE :: UWND(:,:)
      REAL, ALLOCATABLE :: VWND(:,:)
      REAL, ALLOCATABLE :: AIRD(:,:)
      REAL, ALLOCATABLE :: PZIDL(:,:)

      REAL, ALLOCATABLE :: USNEXT(:,:)
      REAL, ALLOCATABLE :: DSNEXT(:,:)
      REAL, ALLOCATABLE :: ADSNEXT(:,:)
      REAL, ALLOCATABLE :: ZIDLNEXT(:,:)

!*     VARIABLE.   TYPE.     PURPOSE.
!      ---------   -------   --------
!      *WSPMIN*    REAL      MINIMUM WIND SPEED ALLOWED IN WAM.
!      *CDA*       CHAR*12   DATE OF LAST WINDFIELD READ IN.
!      *CDATEWO*   CHAR*12   DATE OF NEXT WIND FIELD TO BE READ.
!      *CDAWIFL*   CHAR*12   DATE OF NEXT WIND FILE NAME.
!      *CDATEFL*   CHAR*12   DATE OF NEXT WIND FILE TO BE ACCESSED.
!      *IIG*       INTEGER   BLOCK NUMBER OF LAST WIND FIELD READ IN
!      *NC*        INTGER    NUMBER OF COLUMNS IN INPUT WIND ARRAY
!      *NR*        INTEGER   NUMBER OF ROWS    IN INPUT WIND ARRAY
!      *UWND*      REAL      INPUT U WIND GRID COMPONENT
!      *VWND*      REAL      INPUT V WIND GRID COMPONENT
!      *AIRD*      REAL      INPUT AIR DENSITY GRID
!      *USNEXT*    REAL      TEMPORARY STORAGE OF WIND SPEED
!      *DSNEXT*    REAL      TEMPORARY STORAGE OF WIND DIRECTION
!      *ROAIRNEXT* REAL      TEMPORARY STORAGE OF AIR DENSITY 

! ----------------------------------------------------------------------

      END MODULE YOWWIND
