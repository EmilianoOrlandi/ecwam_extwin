      MODULE YOWSPEC 

      IMPLICIT NONE

      INTEGER, ALLOCATABLE :: NSTART(:) 
      INTEGER, ALLOCATABLE :: NEND(:) 
      INTEGER, ALLOCATABLE :: KLENTOP(:) 
      INTEGER, ALLOCATABLE :: KLENBOT(:) 
      REAL, ALLOCATABLE :: U10NEW(:) 
      REAL, ALLOCATABLE :: U10OLD(:,:) 
      REAL, ALLOCATABLE :: THWNEW(:) 
      REAL, ALLOCATABLE :: THWOLD(:,:) 
      REAL, ALLOCATABLE :: USNEW(:) 
      REAL, ALLOCATABLE :: USOLD(:,:) 
      REAL, ALLOCATABLE :: Z0NEW(:) 
      REAL, ALLOCATABLE :: Z0OLD(:,:) 
      REAL, ALLOCATABLE :: TAUW(:,:) 
      REAL, ALLOCATABLE :: FCONST(:,:) 
      REAL, ALLOCATABLE, DIMENSION(:,:,:) :: FL1 
      REAL, ALLOCATABLE, DIMENSION(:,:,:) :: FL2 
      REAL, ALLOCATABLE, DIMENSION(:,:,:) :: FL3 
      REAL, ALLOCATABLE, DIMENSION(:,:,:) :: SL 

!     *NSTART*    INDEX OF THE FIRST POINT OF THE SUB GRID DOMAIN
!     *NEND*      INDEX OF THE LAST POINT OF THE SUB GRID DOMAIN
!     *U10NEW*    NEW WIND SPEED IN M/S.
!     *U10OLD*    INTERMEDIATE STORAGE OF MODULUS OF WIND SPEED.
!     *THWNEW*    WIND DIRECTION IN RADIANS IN OCEANOGRAPHIC
!                 NOTATION (POINTING ANGLE OF WIND VECTOR,
!                 CLOCKWISE FROM NORTH).
!     *THWOLD*    INTERMEDIATE STORAGE OF WIND DIRECTION 
!     *USNEW*     NEW FRICTION VELOCITY IN M/S.
!     *USOLD*     INTERMEDIATE STORAGE OF MODULUS OF FRICTION
!                 VELOCITY.
!     *Z0NEW*     ROUGHNESS LENGTH IN M.
!     *Z0OLD*     INTERMEDIATE STORAGE OF ROUGHNESS LENGTH IN M.
!     *TAUW*      WAVE STRESS IN (M/S)**2
!     *FCONST*    = 1 FOR PROGNOSTIC FREQUENCY BANDS.
!                 = 0 FOR DIAGNOSTIC FREQUENCY BANDS.
!     *FL3*       FREQUENCY SPECTRUM(INPUT AND OUTPUT).
!     *FL2*       FREQUENCY SPECTRUM(INPUT).
!     *FL1*       FREQUENCY SPECTRUM(INPUT).
!     *SL*        TOTAL SOURCE FUNCTION ARRAY.
! ----------------------------------------------------------------------

      END MODULE YOWSPEC 
