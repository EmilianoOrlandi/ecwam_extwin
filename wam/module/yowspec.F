      MODULE YOWSPEC

      IMPLICIT NONE

      INTEGER, ALLOCATABLE :: NSTART(:) 
      INTEGER, ALLOCATABLE :: NEND(:) 
      INTEGER, ALLOCATABLE :: NBLKS(:) 
      INTEGER, ALLOCATABLE :: NBLKE(:) 
      INTEGER, ALLOCATABLE :: KLENTOP(:) 
      INTEGER, ALLOCATABLE :: KLENBOT(:) 
      INTEGER, ALLOCATABLE :: NFROMPE(:) 
      INTEGER :: NFROMPEMAX
      INTEGER, ALLOCATABLE :: NTOPE(:) 
      INTEGER :: NTOPEMAX
      INTEGER, ALLOCATABLE :: NIJSTART(:) 
      INTEGER, ALLOCATABLE :: IJTOPE(:,:) 
      INTEGER :: NGBTOPE
      INTEGER, ALLOCATABLE :: NTOPELST(:) 
      INTEGER :: NGBFROMPE
      INTEGER, ALLOCATABLE :: NFROMPELST(:) 
      INTEGER, ALLOCATABLE :: IJ2NEWIJ(:) 
      REAL, ALLOCATABLE :: U10NEW(:) 
      REAL, ALLOCATABLE :: U10OLD(:,:) 
      REAL, ALLOCATABLE :: THWNEW(:) 
      REAL, ALLOCATABLE :: THWOLD(:,:) 
      REAL, ALLOCATABLE :: USNEW(:) 
      REAL, ALLOCATABLE :: USOLD(:,:) 
      REAL, ALLOCATABLE :: Z0NEW(:) 
      REAL, ALLOCATABLE :: Z0OLD(:,:) 
      REAL, ALLOCATABLE :: TAUW(:,:) 
      REAL, ALLOCATABLE :: BETAOLD(:) 
      REAL, ALLOCATABLE :: ROAIRN(:) 
      REAL, ALLOCATABLE :: ROAIRO(:,:) 
      REAL, ALLOCATABLE :: ZIDLNEW(:) 
      REAL, ALLOCATABLE :: ZIDLOLD(:,:) 
      REAL, ALLOCATABLE, DIMENSION(:,:,:) :: FL1
      REAL, ALLOCATABLE, DIMENSION(:,:,:) :: FL2
      REAL, ALLOCATABLE, DIMENSION(:,:,:) :: FL3
      REAL, ALLOCATABLE, DIMENSION(:,:,:) :: SL

!     *NSTART*    INDEX OF THE FIRST POINT OF THE COMPUTATION SUB GRID DOMAIN
!     *NEND*      INDEX OF THE LAST POINT OF THE COMPUTATION SUB GRID DOMAIN
!     *NBLKS*     INDEX OF THE FIRST POINT OF THE SUB GRID DOMAIN (only local points)
!     *NBLKE*     INDEX OF THE LAST POINT OF THE SUB GRID DOMAIN (only local points)
!     *KLENTOP*   LENGTH OF THE MESSAGE SENT BY THE NEXT
!                 SUB GRID DOMAIN (IN GRID POINT UNIT)
!     *KLENBOT*   LENGTH OF THE MESSAGE SENT BY THE PREVIOUS
!                 SUB GRID DOMAIN (IN GRID POINT UNIT)
!     *NFROMPE*   INDICATES HOW MANY HALO POINTS THE LOCAL PE NEEDS
!                 TO GET FROM THE OTHER PE'S.
!     *NFROMPEMAX*MAXIMUM OF NFROMPE
!     *NTOPE*     INDICATES HOW MANY HALO POINTS THE LOCAL PE NEEDS
!                 TO SEND TO THE OTHER PE'S.
!     *NTOPEMAX*  MAXIMUM OF NTOPE
!     *NIJSTART*  INDEX OF THE FIRST HALO POINT FROM A GIVEN PE
!                 IN THE LOCAL HALO BUFFERS WHICH PADS BOTH ENDS OF
!                 THE LOCAL GRID POINT 1-D ARRAY
!     *IJTOPE*    TELLS THE LOCAL PE THE INDEX OF WHICH POINTS IT NEEDS
!                 TO SEND TO THE OTHER PE'S.
!     *NGBTOPE*   TOTAL NUMBER OF PE's TO WHICH INFORMATION WILL BE
!                 SENT FROM THE LOCAL PE VIA MESSAGE PASSING
!     *NTOPELST*  LIST OF THE PE'S TO WHICH INFORMATION WILL BE SENT
!     *NGBFROMPE* TOTAL NUMBER OF PE's FROM WHICH INFORMATION WILL BE
!                 RECEIVED TO THE LOCAL PE VIA MESSAGE PASSING
!     *NFROMPELST*LIST OF THE PE'S FROM WHICH INFORMATION WILL BE
!                 RECEIVED
!     *IJ2NEWIJ*  TRANSFORMS THE OLD INDEX IJ INTO THE NEW IJ AS
!                 DETERMINED IN THE CASE OF 2-D DECOMPOSITION
!                 (SEE MPDECOMP).
!     *U10NEW*    NEW WIND SPEED IN M/S.
!     *U10OLD*    INTERMEDIATE STORAGE OF MODULUS OF WIND SPEED.
!     *THWNEW*    WIND DIRECTION IN RADIANS IN OCEANOGRAPHIC
!                 NOTATION (POINTING ANGLE OF WIND VECTOR,
!                 CLOCKWISE FROM NORTH).
!     *THWOLD*    INTERMEDIATE STORAGE OF WIND DIRECTION 
!     *USNEW*     NEW FRICTION VELOCITY IN M/S.
!     *USOLD*     INTERMEDIATE STORAGE OF MODULUS OF FRICTION
!                 VELOCITY.
!     *Z0NEW*     ROUGHNESS LENGTH IN M.
!     *Z0OLD*     INTERMEDIATE STORAGE OF ROUGHNESS LENGTH IN M.
!     *TAUW*      WAVE STRESS IN (M/S)**2
!     *BETAOLD*   CHARNOCK COEFFICIENT AT THE BEGINNING OF TIME STEP
!     *FL3*       FREQUENCY SPECTRUM(INPUT AND OUTPUT).
!     *FL2*       FREQUENCY SPECTRUM(INPUT).
!     *FL1*       FREQUENCY SPECTRUM(INPUT).
!     *SL*        TOTAL SOURCE FUNCTION ARRAY.
! ----------------------------------------------------------------------

      END MODULE YOWSPEC 
