      MODULE YOWMAP

      IMPLICIT NONE

!*    ** *MAP*  LON/LAT INDEX OF EACH SEA POINT.

      INTEGER, ALLOCATABLE :: IXLG(:,:) 
      INTEGER, ALLOCATABLE :: KXLT(:,:) 
      INTEGER NX 
      INTEGER NY 
      INTEGER IPER 
      INTEGER IRGG 
      INTEGER IQGAUSS
      INTEGER KMNOP
      INTEGER KMSOP
      INTEGER, PARAMETER :: MIKOFST=4
      INTEGER, ALLOCATABLE :: KXLTMIN(:) 
      INTEGER, ALLOCATABLE :: KXLTMAX(:) 
      INTEGER, ALLOCATABLE :: IFROMIJ(:,:) 
      INTEGER, ALLOCATABLE :: JFROMIJ(:,:) 
      REAL AMOWEP 
      REAL AMOSOP 
      REAL AMOEAP 
      REAL AMONOP 
      REAL XDELLA 
      REAL XDELLO 
      REAL, ALLOCATABLE :: ZDELLO(:) 
      LOGICAL :: LLOBSTRCT
      LOGICAL, ALLOCATABLE :: LLBOUND(:)
      LOGICAL :: LAQUA

!*     VARIABLE.   TYPE.     PURPOSE.
!      ---------   -------   --------
!      *IXLG*      INTEGER   LONG. GRID INDEX.
!      *KXLT*      INTEGER   SOUTH-NORTH LAT. GRID INDEX.
!      *NX*        INTEGER   NUMBER OF LONGITUDES IN GRID.
!      *NY*        INTEGER   NUMBER OF LATITUDES  IN GRID.
!      *IPER*      INTEGER   = 1 IF GRID IS PERIODIC.
!      *IRGG*      INTEGER   GRID CODE: 0 = REGULAR, 1 = IRREGULAR.
!      *IQGAUSS*   INTEGER   =1 IF A QUASI GAUSSIAN GRID IS USED. 
!                            =0 OTHERWISE.
!      *KMNOP*     INTEGER   INDEX FOR THE MOST NORTHERN LATITUDE WITH
!                            MODEL SEA POINTS. IT SHOULD NORMALLY BE 1
!                            EXCEPT IF THE NORHTERN LATITUDES HAVES BEEN
!                            BLANKED (SEE QUASI GAUSSIAN GRID)
!      *KMSOP*     INTEGER   INDEX FOR THE MOST SOUTHERN LATITUDE WITH
!                            MODEL SEA POINTS. IT SHOULD NORMALLY BE NY 
!      *MIKOFST*   INTEGER   PEIRODIC OFFSET FOR ARRAY IJFROMIK USED IN CTUW.
!      *KXLTMIN*   INTEGER   MINIMUM OF KXLT ON EACH PE.
!      *KXLTMAX*   INTEGER   MAXIMUM OF KXLT ON EACH PE.
!      *IFROMIJ*   INTEGER   WEST-EAST INDEX FOR A GIVEN IJ FOR ALL IJ'S LOCAL TO
!                            A GIVEN PROCESSOR AND THEIR HALO.
!                   !!!! THIS IS THE LOCAL VERSION OF IXLG, WHICH IS
!                        DEFINED GLOBALLY, HENCE IXLG IS NOT VALID OVER
!                        THE HALO BECAUSE THE HALO IS ONLY DEFINED LOCALLY !!!
!      *JFROMIJ*   INTEGER   NORTH-SOUTH INDEX FOR A GIVEN IJ FOR ALL IJ'S
!                            LOCAL TO A GIVEN PROCESSOR AND THEIR HALO.
!                            JFROMIJ= NGY-KXLT+1
!                   !!!! THIS IS THE LOCAL VERSION OF NGY-KXLT+1, WHICH IS
!                        DEFINED GLOBALLY, HENCE KXLT IS NOT VALID OVER
!                        THE HALO BECAUSE THE HALO IS ONLY DEFINED LOCALLY !!!
!      *AMOWEP*    REAL      MOST WESTERN LONGITUDE IN GRID (DEGREE).
!      *AMOSOP*    REAL      MOST SOUTHERN LATITUDE IN GRID (DEGREE).
!      *AMOEAP*    REAL      MOST EASTERN LONGITUDE IN GRID (DEGREE).
!      *AMONOP*    REAL      MOST NORTHERN LATITUDE IN GRID (DEGREE).
!      *XDELLA*    REAL      GRID INCREMENT FOR LATITUDE (DEGREE).
!      *XDELLO*    REAL      CONSTANT GRID INCREMENT FOR LONGITUDE (DEG)
!      *ZDELLO*    REAL      VARIABLE GRID INCREMENT FOR LONGITUDE (DEG)
!      *LLOBSTRCT* LOGICAL   CONTROLS WHETHER THE NEW TYPE OF BATHYMETRY
!                            INPUT IS USED IN CONJUNCTION WITH THE
!                            OBSTRUCTION COEFFICIENT IN THE ADVECTION.
!      *LLBOUND*   LOGICAL   TRUE IS THE GRID POINT IS ON THE BOUNDARY OF
!                            THE DOMAIN.
!      *LAQUA*     LOGICAL   TRUE IF MODEL IN AQUA PLANET CONFIGURATION
!                            (i.e. NO LAND AND DEEP WATER).

! ----------------------------------------------------------------------
      END MODULE YOWMAP
