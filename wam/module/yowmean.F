      MODULE YOWMEAN

      USE YOWNEMOP

      IMPLICIT NONE

!*    **  *MEANPA* - INTEGRATED PARAMETERS OF A BLOCK.

      REAL,ALLOCATABLE :: EMEAN(:) 
      REAL,ALLOCATABLE :: FMEAN(:) 
      REAL(NEMODP),ALLOCATABLE :: NSWH(:) 
      REAL(NEMODP),ALLOCATABLE :: NMWP(:) 
      REAL,ALLOCATABLE :: THQ(:) 
      REAL,ALLOCATABLE :: SMEAN(:) 
      REAL,ALLOCATABLE :: ALTWH(:) 
      REAL,ALLOCATABLE :: CALTWH(:) 
      REAL,ALLOCATABLE :: RALTCOR(:) 
      REAL,ALLOCATABLE :: P1MEAN(:) 
      REAL,ALLOCATABLE :: P2MEAN(:) 
      REAL,ALLOCATABLE :: SPRDMEAN(:) 
      REAL,ALLOCATABLE :: C3MEAN(:) 
      REAL,ALLOCATABLE :: C4MEAN(:) 
      REAL,ALLOCATABLE :: BFMEAN(:) 
      REAL,ALLOCATABLE :: QPMEAN(:) 
      REAL,ALLOCATABLE :: HMAXMEAN(:) 
      REAL,ALLOCATABLE :: FPMEAN(:) 
      REAL,ALLOCATABLE :: TMAXMEAN(:) 
      REAL,ALLOCATABLE :: ETAMEAN(:) 
      REAL,ALLOCATABLE :: USTMEAN(:) 
      REAL,ALLOCATABLE :: VSTMEAN(:) 
      REAL,ALLOCATABLE :: PHIEPS(:) 
      REAL(NEMODP),ALLOCATABLE :: NPHIEPS(:) 
      REAL,ALLOCATABLE :: PHIAW(:) 
      REAL,ALLOCATABLE :: TAUOC(:) 
      REAL(NEMODP),ALLOCATABLE :: NTAUOC(:) 
      REAL,ALLOCATABLE :: STRNMS(:) 
      REAL,ALLOCATABLE :: E10MEAN(:) 
      REAL,ALLOCATABLE :: WEFLXM(:) 
      REAL,ALLOCATABLE :: WEFLXD(:) 
      REAL,ALLOCATABLE :: E1012(:) 
      REAL,ALLOCATABLE :: E1214(:) 
      REAL,ALLOCATABLE :: E1417(:) 
      REAL,ALLOCATABLE :: E1721(:) 
      REAL,ALLOCATABLE :: E2125(:) 
      REAL,ALLOCATABLE :: E2530(:) 
      REAL,ALLOCATABLE :: WX1(:)
      REAL,ALLOCATABLE :: WX2(:)
      REAL,ALLOCATABLE :: WX3(:)
      REAL,ALLOCATABLE :: WX4(:)
      REAL,ALLOCATABLE :: WX5(:)

!*     VARIABLE.      TYPE.     PURPOSE.
!      ---------      -------   --------
!      *EMEAN*        REAL      TOTAL ENERGY.
!      *FMEAN*        REAL      MEAN FREQUENCY (1./MEAN PERIOD).
!      *NSWH*         REAL      SIGNIFICANT WAVE HEIGHT FOR NEMO
!      *NMWP*         REAL      MEAN WAVE PERIOD FOR NEMO
!      *THQ*          REAL      MEAN WAVE DIRECTION IN RADIANS.
!      *SMEAN*        REAL      MEAN SQUARE SLOPE.
!      *ALTWH*        REAL      ALTIMETER WAVE HEIGHT 
!      *CALTWH*       REAL      CORRECTED ALTIMETER WAVE HEIGHT 
!      *RALTCOR*      REAL      ALTIMETER RANGE CORRECTION 
!      *P1MEAN*       REAL      MEAN PERIOD (1).
!      *P2MEAN*       REAL      MEAN PERIOD (2).
!      *SPRDMEAN*     REAL      MEAN DIRECTIONAl SPREAD.
!      *C3MEAN*       REAL      SURFACE ELEVATION SKEWNESS.
!      *C4MEAN*       REAL      SURFACE ELEVATION KURTOSIS.
!      *BFMEAN*       REAL      BENJAMIN-FEIR INDEX.
!      *QPMEAN*       REAL      SPECTRAL PEAKEDNESS.
!      *HMAXMEAN*     REAL      MAXIMUM WAVE HEIGHT.
!      *FPMEAN*       REAL      PEAK FREQUENCY FROM 1D SPECTRUM.
!      *TMAXMEAN*     REAL      MAXIMUM PERIOD CORRESPONDING TO MAXIMUM
!                               WAVE HEIGHT.
!      *ETAMEAN*      REAL      WAVE-INDUCED MEAN SURFACE ELEVATION. 
!      *USTMEAN*      REAL      U-COMP STOKES DRIFT.
!      *VSTMEAN*      REAL      V-COMP STOKES DRIFT.
!      *PHIEPS*       REAL      NORMALIZED ENERGY FLUX TO OCEAN.
!      *NPHIEPS*      REAL      COPY OF PHIEPS FOR NEMO
!      *PHIAW*        REAL      NORMALIZED ENERGY FLUX FROM WIND TO WAVES.
!      *TAUOC*        REAL      NORMALIZED MOMENTUM FLUX INTO OCEAN.
!      *NTAUOC*       REAL      COPY OF TAUOC FOR NEMO.
!      *STRNMS*       REAL      MEAN SQUARE STRAIN INTO THE SEA ICE.
!      *E10MEAN*      REAL      SPECTRAL VARIANCE WITH PERIOD > 10s  
!      *WEFLXM*       REAL      WAVE ENERGY FLUX MAGNITUDE
!      *WEFLXD*       REAL      WAVE ENERGY FLUX MEAN DIRECTION 
!      *E1012*        REAL      SPECTRAL VARIANCE WITH PERIOD BETWEEN 10 AND 12 SECONDS
!      *E1214*        REAL      SPECTRAL VARIANCE WITH PERIOD BETWEEN 12 AND 14 SECONDS
!      *E1417*        REAL      SPECTRAL VARIANCE WITH PERIOD BETWEEN 14 AND 17 SECONDS
!      *E1721*        REAL      SPECTRAL VARIANCE WITH PERIOD BETWEEN 17 AND 21 SECONDS
!      *E2125*        REAL      SPECTRAL VARIANCE WITH PERIOD BETWEEN 21 AND 25 SECONDS
!      *E2530*        REAL      SPECTRAL VARIANCE WITH PERIOD BETWEEN 25 AND 30 SECONDS

! ----------------------------------------------------------------------
      END MODULE YOWMEAN
