      MODULE YOWCOUT

      IMPLICIT NONE

!*    ** *COUT*  OUTPUT POINTS INDICES AND FLAGS.

      INTEGER, PARAMETER :: JPPFLAG=46 
      INTEGER, PARAMETER :: NREAL=9 
      INTEGER, PARAMETER :: NINTGR=1 

      CHARACTER*14, ALLOCATABLE :: COUTT(:)
      CHARACTER*14 COUTLST 
      CHARACTER*14, ALLOCATABLE :: COUTS(:) 
      CHARACTER*14, ALLOCATABLE :: CASS(:) 

      LOGICAL FFLAG(JPPFLAG) 
      LOGICAL FFLAG20 
      LOGICAL FFLAG21 
      LOGICAL FFLAG25 
      LOGICAL FFLAG26 
      LOGICAL FFLAG27 
      LOGICAL PFLAG(JPPFLAG) 
      LOGICAL PFLAG20 
      LOGICAL PFLAG21 
      LOGICAL PFLAG25 
      LOGICAL PFLAG26 
      LOGICAL PFLAG27 
      LOGICAL GFLAG(JPPFLAG) 
      LOGICAL GFLAG20 
      LOGICAL GFLAG21 
      LOGICAL GFLAG25 
      LOGICAL GFLAG26 
      LOGICAL GFLAG27 
      LOGICAL NFLAG(JPPFLAG) 
      LOGICAL NFLAGALL 
      LOGICAL UFLAG(JPPFLAG) 
      LOGICAL LFDB 
      LOGICAL LOUTINT 
      LOGICAL LOUTSPP 
      LOGICAL :: LWFLUXOUT
      LOGICAL :: LSECONDORDER
      LOGICAL :: LGRIBAPI_I
      LOGICAL :: LGRIBAPI_O
      LOGICAL :: LRSTST0

      INTEGER NGOUT 
      INTEGER, ALLOCATABLE :: IGAR(:) 
      INTEGER, ALLOCATABLE :: IJAR(:) 
      INTEGER NOUTT 
      INTEGER NOUTS 
      INTEGER NASS 
      INTEGER IPFGTBL(JPPFLAG+1) 
      INTEGER, PARAMETER :: KDEL=1
      INTEGER, PARAMETER :: MDEL=1

!*     VARIABLE.   TYPE.     PURPOSE.
!      ---------   -------   --------
!      *NREAL*     INTEGER   NUMBER OF REAL FIELDS IN RESTART FILE
!      *NINTGR*    INTEGER   NUMBER OF INTEGER FIELDS IN RESTART FILE
!      *COUTT*     CHAR*14   OUTPUT TIMES.
!      *COUTLST*   CHAR*14   LAST OUTPUT TIME.
!      *COUTS*     CHAR*14   OUTPUT TIMES FOR THE SPECTRA.
!      *CASS*      CHAR*14   ASSIMILATION TIMES.
!      *FFLAG*     LOGICAL   FILE  OUTPUT FLAG FOR EACH OUTPUT TYPE.
!      *FFLAG20*   LOGICAL   .TRUE. IF OUTPUT IS WRITTEN TO UNIT IU20.
!      *FFLAG21*   LOGICAL   .TRUE. IF OUTPUT IS WRITTEN TO UNIT IU21.
!      *FFLAG25*   LOGICAL   .TRUE. IF OUTPUT IS WRITTEN TO UNIT IU25.
!      *FFLAG26*   LOGICAL   .TRUE. IF OUTPUT IS WRITTEN TO UNIT IU26.
!      *FFLAG27*   LOGICAL   NOT USED.
!      *PFLAG*     LOGICAL   PRINT OUTPUT FLAG FOR EACH OUTPUT TYPE.
!      *PFLAG20*   LOGICAL   .TRUE. IF PRINT OUTPUT OF FIELDS.
!      *PFLAG21*   LOGICAL   .TRUE. IF PRINT OUTPUT OF SWELL FIELDS.
!      *PFLAG25*   LOGICAL   .TRUE. IF PRINT OUTPUT OF SPECTRA.
!      *PFLAG26*   LOGICAL   .TRUE. IF PRINT OUTPUT OF SWELL SPECTRA.
!      *PFLAG27*   LOGICAL   NOT USED.
!      *GFLAG*     LOGICAL   GRIB  OUTPUT FLAG FOR EACH OUTPUT TYPE.
!      *GFLAG20*   LOGICAL   .TRUE. IF OUTPUT IS GRIBBED TO UNIT IU30.
!      *GFLAG21*   LOGICAL   .TRUE. IF OUTPUT IS GRIBBED TO UNIT IU31.
!      *GFLAG25*   LOGICAL   .TRUE. IF OUTPUT IS GRIBBED TO UNIT IU35.
!      *GFLAG26*   LOGICAL   .TRUE. IF OUTPUT IS GRIBBED TO UNIT IU36.
!      *GFLAG27*   LOGICAL   NOT USED.
!      *NFLAG*     LOGICAL   NORM OUTPUT FLAG FOR EACH OUTPUT TYPE.
!      *NFLAGALL*  LOGICAL   .TRUE. IF NORM OUTPUT IS REQUESTED.
!      *UFLAG*     LOGICAL   FLAG WHETHER OUTPUT GRID FIELD IS USED
!                            IN THE ASSIMILATION SCHEME
!      *LFDB*      LOGICAL   .TRUE. IF OUTPUT IS SENT TO FDB.
!      *LOUTINT*   LOGICAL   .TRUE. IF OUTINT WAS CALLED.
!      *LOUTSPP*   LOGICAL   .TRUE. IF OUTSPP WAS CALLED.
!      *NGOUT*     INTEGER   NUMBER OF OUTPUT POINTS.
!      *IGAR*      INTEGER   BLOCK NUMBER OF OUTPUT POINTS.
!      *IJAR*      INTEGER   GRIDPOINT NUMBER OF OUTPUT POINT.
!      *NOUTT*     INTEGER   NUMBER OF OUTPUT TIMES.
!      *NOUTS*     INTEGER   NUMBER OF OUTPUT TIMES FOR THE SPECTRA.
!      *NASS*      INTEGER   NUMBER OF ASSIMILATION TIMES.
!      *KDEL*      INTEGER   NUMBER OF DIRECTIONS THAT WILL BE WRITTEN
!                            OUT/READ IN WHEN BINARY SPECTRA ARE PRODUCED
!      *MDEL*      INTEGER   NUMBER OF FREQUENCIES THAT WILL BE WRITTEN
!                            OUT/READ IN WHEN BINARY SPECTRA ARE PRODUCED
!      *LWFLUXOUT* LOGICAL   TRUE IF THE OCEAN FLUXES ARE OUTPUT PARAMETERS.
!      *LSECONDORDER*        TRUE IF SECOND ORDER CORRECTION IS COMPUTED
!                            FOR OUTPUT INTEGRATED PARAMETERS.
!      *LGRIBAPI_I LOGICAL   TRUE IF GRIB API IS USED AT INPUT.
!      *LGRIBAPI_O LOGICAL   TRUE IF GRIB API IS USED AT OUTPUT.
!      *LRSTST0    LOGICAL   TRUE IF GRIB HEADER HAVE TO BE RESET
!                            SUCH THAT THE FORECAST STEP POINTS TO
!                            THE START OF THE RUN.
! ----------------------------------------------------------------------
      END MODULE YOWCOUT
