      MODULE YOWSWEL

      IMPLICIT NONE

!*    ** *SWELL* - SEA AND SWELL PARAMETERS.

      REAL, ALLOCATABLE :: ESWELL(:) 
      REAL, ALLOCATABLE :: FSWELL(:) 
      REAL, ALLOCATABLE :: THSWELL(:) 
      REAL, ALLOCATABLE :: P1SWELL(:) 
      REAL, ALLOCATABLE :: P2SWELL(:) 
      REAL, ALLOCATABLE :: SPRDSWELL(:) 
      REAL, ALLOCATABLE :: ESEA(:) 
      REAL, ALLOCATABLE :: FSEA(:) 
      REAL, ALLOCATABLE :: THWISEA(:) 
      REAL, ALLOCATABLE :: P1SEA(:) 
      REAL, ALLOCATABLE :: P2SEA(:) 
      REAL, ALLOCATABLE :: SPRDSEA(:) 

!*     VARIABLE.   TYPE.     PURPOSE.
!      ---------   -------   --------
!      *ESWELL*    REAL      TOTAL SWELL ENERGY
!      *FSWELL*    REAL      MEAN SWELL FREQUENCY
!      *THSWELL*   REAL      MEAN SWELL DIRECTION
!      *P1SWELL*   REAL      MEAN SWELL PERIOD (1)
!      *P2SWELL*   REAL      MEAN SWELL PERIOD (2)
!      *SPRDSWELL* REAL      MEAN SWELL DIRECTIONAL SPREAD 
!      *ESEA*      REAL      TOTAL WIND SEA ENERGY
!      *FSEA*      REAL      MEAN WIND SEA FREQUENCY
!      *THWISEA*   REAL      MEAN WIND INTERACTING SEA DIRECTION
!      *P1SEA*     REAL      MEAN WIND SEA PERIOD (1)
!      *P2SEA*     REAL      MEAN WIND SEA PERIOD (2)
!      *SPRDSEA*   REAL      MEAN WIND SEA DIRECTIONAL SPREAD 

! ----------------------------------------------------------------------
      END MODULE YOWSWEL
